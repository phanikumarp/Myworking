package n42.services.model.info;

public class ModelDefinitionException extends Exception {

	private static final long serialVersionUID = 1587054890312451320L;

	public ModelDefinitionException() {
		super();
	}

	public ModelDefinitionException(String message) {
		super(message);
	}

	public ModelDefinitionException(String message, Throwable cause) {
		super(message, cause);
	}

	public ModelDefinitionException(Throwable cause) {
		super(cause);
	}

}
